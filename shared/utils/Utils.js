export function getFullName (unidade) {
  switch (unidade) {
    case 'homero':
      return 'HOMERO MASSENA';
    case 'cora':
      return 'CORA CORALINA';
    case 'augusto':
      return 'AUGUSTO RUSCHI';
    case 'monteiro':
      return 'MONTEIRO LOBATO';
    case 'rubem':
      return 'RUBEM BRAGA';
    default:
      return;
  }
};
